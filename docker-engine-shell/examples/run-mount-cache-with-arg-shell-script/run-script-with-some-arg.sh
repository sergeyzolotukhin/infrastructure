#!/bin/sh

export DOCKER_BUILDKIT=1

docker system prune --all --volumes -f

clear

current_date=$( date '+%F_%H:%M:%S' )

echo "================================================================================================================"
echo "Run with empty cache"
echo "================================================================================================================"

docker build \
  --build-arg CURRENT_DATE="${current_date}" \
  --progress=plain \
  .

echo "================================================================================================================"
echo "Second run with same arguments"
echo "================================================================================================================"

docker build \
  --progress=plain \
  --build-arg CURRENT_DATE="${current_date}" \
  .

echo "================================================================================================================"